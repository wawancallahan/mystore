    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (txtNama.getText().equals("") || txtUsername.getText().equals("") || txtPassword.getPassword().equals("") || cmbJenis.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Semua Input Harus Terisi", "Peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<String> request = new ArrayList<>();
        request.add(0, txtNama.getText());
        request.add(1, txtUsername.getText());
        request.add(2, String.valueOf(txtPassword.getPassword()));
        request.add(3, cmbJenis.getSelectedItem().toString());

        boolean update = this.userModel.update(request, this.id);

        if (update) {
            this.dispose();
            
            UserForm.showForm().fillTable();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Terjadi Kesalahan Proses Simpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

