    private UserEdit setModelForm() {
        if (this.id != null) {
            UserLib item = userModel.findItem(this.id);
            
            if (item != null) {
                txtNama.setText(item.getName());
                txtUsername.setText(item.getUsername());
                txtPassword.setText(item.getPassword());
                cmbJenis.setSelectedItem(item.getType());
            }
        }
        
        return this;
    }

